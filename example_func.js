let multi = function (p1, p2) {
  return p1 * p2;
};

const div = (p1, p2) => {
  return p1 / p2;
};

const plus = (p1, p2) => {
  return p1 + p2;
};

const minus = (p1, p2) => {
  return p1 - p2;
};

//work#1 arrow function 으로 plus, minus method 기능을 작성해 주세요

console.log(multi(1, 56));
console.log(div(10, 2));
console.log(plus(10, 2));
console.log(minus(10, 2));
